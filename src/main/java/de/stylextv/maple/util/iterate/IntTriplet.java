package de.stylextv.maple.util.iterate;

public class IntTriplet {
	
	private int i;
	private int j;
	private int k;
	
	public IntTriplet(int i, int j, int k) {
		this.i = i;
		this.j = j;
		this.k = k;
	}
	
	public int getFirst() {
		return i;
	}
	
	public int getSecond() {
		return j;
	}
	
	public int getThird() {
		return k;
	}
	
}
